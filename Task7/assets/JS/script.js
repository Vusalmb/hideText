$(document).ready(function(){
    $("#text-1").click(function(){
        $("#text-item-1").show();
        $("#menu-desc li").css("flex-wrap", "wrap");

        $("#down1").hide();
        $("#up1").show();
        $("#text-1").show();
        $("#menu-desc li").css("flex-wrap", "wrap");
    });
});

